// Package interceptors предоставляет набор gRPC-интерцепторов для обработки вызовов.
//
// Включает следующие интерцепторы:
//
//   - **Аутентификация**: JWT-based аутентификация через подпакет jwt
//
//   - Автоматическая генерация токенов
//
//   - Проверка валидности токенов
//
//   - Извлечение userID в контекст вызова
//
//   - Передача токенов через метаданные gRPC
//
//   - **Авторизация**: Контроль доступа через подпакет authz
//
//   - Проверка доверенных подсетей (X-Real-IP/X-Forwarded-For)
//
//   - **Логирование**: Детальное логирование вызовов через подпакет logger
//
//   - Полные имена методов (FullMethod)
//
//   - Коды статусов gRPC
//
//   - Время выполнения
//
//   - Метаданные запросов
//
//   - Обработка ошибок
//
// Все интерцепторы:
//   - Поддерживают цепочки вызовов (chaining)
//   - Совместимы со стандартными интерцепторами gRPC
//   - Позволяют тонкую настройку поведения
//   - Могут применяться выборочно к методам
//
// Пример использования:
//
//	server := grpc.NewServer(
//	    grpc.ChainUnaryInterceptor(
//	        interceptor.JWT([]byte(secret)),
//	        interceptor.TrustedSubnet("192.168.1.0/24"),
//	        interceptor.Logging(zapLogger),
//	    ),
//	)
