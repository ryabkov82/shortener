// Package middleware предоставляет набор HTTP-middleware для обработки запросов.
//
// Включает следующие middleware:
//
//   - **Аутентификация**: JWT-based аутентификация через подпакет auth
//
//   - Автоматическая генерация токенов
//
//   - Проверка валидности токенов
//
//   - Извлечение userID в контекст запроса
//
//   - **Логирование**: Подробное логирование запросов через подпакет logger
//
//   - Метод запроса
//
//   - URL пути
//
//   - Коды статусов ответа
//
//   - Время выполнения
//
//   - Размер ответа
//
//   - **Сжатие**: GZIP-компрессия через подпакет mwgzip
//
//   - Прозрачное сжатие ответов
//
//   - Распаковка входящих запросов
//
//   - Поддержка пулов reader/writer
//
// Все middleware поддерживают цепочки вызовов и могут быть кастомизированы.
package middleware
