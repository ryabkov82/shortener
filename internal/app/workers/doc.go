// Package workers реализует фоновые процессы и асинхронные задачи сервиса.
//
// Основные компоненты:
//
//   - **DeleteWorker**: Пул воркеров для асинхронного удаления URL
//
//   - Пакетная обработка задач удаления
//
//   - Настраиваемый размер батча и интервал
//
//   - Graceful shutdown
//
//   - **TaskQueue**: Очередь задач для фоновой обработки
//
//   - Буферизованный канал задач
//
//   - Контроль переполнения очереди
//
//   - Статистика выполнения
//
// Пример использования DeleteWorker:
//
//	// Инициализация
//	worker := workers.NewDeleteWorker(
//	    4,              // кол-во воркеров
//	    100,            // размер батча
//	    time.Minute,    // интервал обработки
//	    storage,        // реализация хранилища
//	)
//	worker.Start()
//
//	// Отправка задач
//	worker.Submit(workers.DeleteTask{
//	    UserID:    "123",
//	    ShortURLs: []string{"abc", "def"},
//	})
//
//	// Остановка
//	worker.GracefulStop(5 * time.Second)
//
// Особенности реализации:
//   - Пул воркеров с настраиваемым параллелизмом
//   - Автоматическая агрегация задач по пользователям
//   - Поддержка graceful shutdown
//   - Логирование ошибок
package workers
