// Package testhandlers предоставляет тесты для HTTP-обработчиков сервиса сокращения URL.
//
// Пакет содержит как юнит-тесты, так и интеграционные тесты для всех основных обработчиков API:
//   - Юнит-тесты: проверка изолированной работы обработчиков
//   - Интеграционные тесты: проверка взаимодействия с хранилищем и другими компонентами
//
// Пакет включает:
//   - Полноценные тесты API с реальными HTTP-запросами
//   - Поддержку тестирования аутентификации через JWT
//   - Проверку всех кейсов использования обработчиков
//
// Особенности:
//   - Использует testcontainers для интеграционных тестов с БД
//   - Поддерживает мокирование через интерфейс Repository
//   - Включает проверки для middleware (gzip, logging, auth)
//
// Пример использования для юнит-тестов:
//
//	func TestShortenHandler_Unit(t *testing.T) {
//	    mockRepo := new(MockRepository) // мок хранилища
//	    testhandlers.TestShortenURL(t, mockRepo)
//	}
//
// Пример использования для интеграционных тестов:
//
//	func TestShortenHandler_Integration(t *testing.T) {
//	    db := setupTestDB() // реальная БД для тестов
//	    testhandlers.TestShortenURL(t, db)
