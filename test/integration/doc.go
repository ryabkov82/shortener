// Package integration предоставляет инфраструктуру для интеграционного тестирования сервиса сокращения URL.
//
// Основные возможности:
//   - Запуск и управление тестовыми контейнерами (PostgreSQL через testcontainers)
//   - Настройка тестового окружения с изолированной БД
//   - Автоматическая подготовка и очистка тестовых данных
//   - Общие утилиты для интеграционных тестов
//
// Структура пакета:
//   - TestMain: точка входа для настройки тестового окружения
//   - Вспомогательные функции для работы с тестовыми данными
//   - Интеграционные тесты основных компонентов системы
//
// Особенности работы:
//   - Для каждого тестового запуска создается изолированный контейнер PostgreSQL
//   - DSN строка подключения доступна через переменную TEST_DB_DSN
//   - Все тесты выполняются на чистой БД (без утечки состояния между тестами)
//
// Пример использования:
//
//	func TestMyIntegration(t *testing.T) {
//	    dsn := os.Getenv("TEST_DB_DSN")
//	    // Инициализация сервиса с тестовой БД
//	    service := NewService(dsn)
//	    // Выполнение тестовых сценариев
//	}
//
// Требования:
//   - Установленный Docker
//   - Доступ к образам PostgreSQL
//
// Примечания:
//   - Для работы тестов требуется запущенный Docker демон
//   - Рекомендуется использовать для тестирования репозиториев и сервисов
//   - Не предназначен для unit-тестов
package integration
